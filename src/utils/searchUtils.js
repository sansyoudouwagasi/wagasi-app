// ひらがなをカタカナに変換する
export const hiraganaToKatakana = (str) => {
  return str.replace(/[\u3041-\u3096]/g, (match) => {
    return String.fromCharCode(match.charCodeAt(0) + 0x60);
  });
};

// カタカナをひらがなに変換する
export const katakanaToHiragana = (str) => {
  return str.replace(/[\u30A1-\u30F6]/g, (match) => {
    return String.fromCharCode(match.charCodeAt(0) - 0x60);
  });
};

/**
 * 検索クエリを正規化・拡張し、検索候補ワードの配列を返す
 */
export const expandSearchQuery = (query) => {
  const q = query.trim().toLowerCase();
  if (!q) return [];

  // 半角カタカナなどを正規化する場合はここでできるが、簡単な置換で十分
  
  const querySet = new Set([q]);

  // 入力された文字のひらがな・カタカナ変換を配列に追加
  const hira = katakanaToHiragana(q);
  const kata = hiraganaToKatakana(q);
  querySet.add(hira);
  querySet.add(kata);

  // 専門用語系の置換 (部分一致でも展開する)
  const currentQueries = Array.from(querySet);
  currentQueries.forEach((cq) => {
    for (const [key, synonyms] of Object.entries(wagashiSynonyms)) {
      if (cq.includes(key)) {
        synonyms.forEach(syn => {
          querySet.add(cq.replace(key, syn));
          // その類義語自体のカタカナ・ひらがなも追加登録しておく
          querySet.add(katakanaToHiragana(syn));
          querySet.add(hiraganaToKatakana(syn));
        });
      }
    }
  });

  return Array.from(querySet).filter(Boolean);
};
