/**
 * 食材名から含まれそうなアレルゲンを自動推測する
 * @param {string} name 食材の名称
 * @returns {string[]} アレルゲンの配列 (e.g. ["小麦", "卵"])
 */
export const detectAllergensFromText = (name) => {
  if (!name) return [];
  const result = [];
  const lowerName = name.toLowerCase();
  
  for (const [allergen, keywords] of Object.entries(ALLERGEN_KEYWORD_MAP)) {
    if (keywords.some(kw => lowerName.includes(kw))) {
      result.push(allergen);
    }
  }
  
  return result;
};
